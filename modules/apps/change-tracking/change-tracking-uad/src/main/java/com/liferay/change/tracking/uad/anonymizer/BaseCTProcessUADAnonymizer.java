package com.liferay.change.tracking.uad.anonymizer;

import com.liferay.change.tracking.model.CTProcess;
import com.liferay.change.tracking.service.CTProcessLocalService;
import com.liferay.change.tracking.uad.constants.CTUADConstants;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;

import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the ct process UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link CTProcessUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseCTProcessUADAnonymizer
	extends DynamicQueryUADAnonymizer<CTProcess> {
	@Override
	public void autoAnonymize(CTProcess ctProcess, long userId,
		User anonymousUser) throws PortalException {
		if (ctProcess.getUserId() == userId) {
			ctProcess.setUserId(anonymousUser.getUserId());
			ctProcess.setUserName(anonymousUser.getFullName());
		}

		ctProcessLocalService.updateCTProcess(ctProcess);
	}

	@Override
	public void delete(CTProcess ctProcess) throws PortalException {
		ctProcessLocalService.deleteCTProcess(ctProcess);
	}

	@Override
	public Class<CTProcess> getTypeClass() {
		return CTProcess.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return ctProcessLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return CTUADConstants.USER_ID_FIELD_NAMES_CT_PROCESS;
	}

	@Reference
	protected CTProcessLocalService ctProcessLocalService;
}